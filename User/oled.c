/*
* @name		OLED_ShowNum_1206
* @description	Show a number in float on OLED screen.
* @inputval	None
* @outputval	None
* @retval 	0
*/
void OLED_ShowNum_1206(uint8_t x, uint8_t y, float num, uint8_t mode)
{
	uint8_t  numStr[10] = "", strCnt = 0, p;
	uint32_t numInt = (uint32_t)(num * 100);
	
	while (numInt != 0) 
	{
            numInt /= 10;
            strCnt++;
	}
	strCnt += 1;
	numStr[strCnt] = '\0';
	numInt = (uint32_t)(num * 100);
	
	p = strCnt - 1;
	while (numInt != 0)
	{
		numStr[p--] = (numInt % 10) + '0';
		numInt /= 10;
		if (p == strCnt - 3)
			numStr[p--] = '.';
	}
	
	OLED_ShowString_1206(x, y, numStr, mode);
}

