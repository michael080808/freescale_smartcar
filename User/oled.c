//OLED的显存
/*存放格式如下.
[0]0 1 2 3 ... 127	
[1]0 1 2 3 ... 127	
[2]0 1 2 3 ... 127	
[3]0 1 2 3 ... 127	
[4]0 1 2 3 ... 127	
[5]0 1 2 3 ... 127	
[6]0 1 2 3 ... 127	
[7]0 1 2 3 ... 127 	
*/	   
uint8_t OLED_GRAM[128][8];	 



/*
* @name		OLED_WR_Byte
* @description	向SSD1306写入一个字节。
* @inputval	dat:要写入的数据/命令
* 		cmd:数据/命令标志 0,表示命令;1,表示数据;
* @outputval	None
* @retval 	0
*/ 
void OLED_WR_Byte(uint8_t dat,uint8_t cmd)
{	
	uint8_t i;			  
	OLED_RS=cmd; //写命令 
	OLED_CS=0;		  
	for(i=0;i<8;i++)
	{			  
		OLED_SCLK=0;
		if(dat&0x80)OLED_SDIN=1;
		else OLED_SDIN=0;
		OLED_SCLK=1;
      		dat<<=1;   
      	}				 
	OLED_CS=1;		  
	OLED_RS=1;   	  
} 


/*
* @name		OLED_Refresh_Gram
* @description	Refresh the video memory on OLED screen.(更新OLED显存)
* @inputval	None
* @outputval	None
* @retval 	0
*/ 	 
void OLED_Refresh_Gram(void)
{
	uint8_t i,n;		    
	for(i=0;i<8;i++)  
	{  
		OLED_WR_Byte (0xb0+i,OLED_CMD);    //设置页地址（0~7）
		OLED_WR_Byte (0x02,OLED_CMD);      //设置显示位置―列低地址,偏移了2列
		OLED_WR_Byte (0x10,OLED_CMD);      //设置显示位置―列高地址   
		for(n=0;n<128;n++)
			OLED_WR_Byte(OLED_GRAM[(n+1)%128][i],OLED_DATA); 
	}   
}


	  	  
/*
* @name		OLED_Display_On
* @description	Open the OLED screen.
* @inputval	None
* @outputval	None
* @retval 	0
*/   
void OLED_Display_On(void)
{
	OLED_WR_Byte(0X8D,OLED_CMD);  //SET DCDC命令
	OLED_WR_Byte(0X14,OLED_CMD);  //DCDC ON
	OLED_WR_Byte(0XAF,OLED_CMD);  //DISPLAY ON
}


